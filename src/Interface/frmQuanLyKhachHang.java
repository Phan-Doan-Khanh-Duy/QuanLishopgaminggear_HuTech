    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try{
            int row = this.jTable1.getSelectedRow();
            String ml =(this.jTable1.getModel().getValueAt(row,0)).toString();
            ResultSet rs = kh.ShowKH(ml);
            if(rs.next())
            {
                this.id.setText(rs.getString("IDKH"));
                this.hoten.setText(rs.getString("HoTen"));
                this.sdt.setText(rs.getString("SDT"));
                this.gioitinh.setText(rs.getString("Email"));
            }
        }
        catch(Exception ex)
        {
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

