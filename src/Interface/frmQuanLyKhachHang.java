    private void luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuActionPerformed
      String idkh = id.getText();
        String ht = hoten.getText();
        String sodienthoai = sdt.getText();
         String gt = gioitinh.getText();

        if(idkh.length() == 0 ||ht.length()==0||sodienthoai.length()==0|gt.length()==0)
        {
            JOptionPane.showMessageDialog(null,"Hãy nhập đầy đủ thông tin");
        }
      

        else {
            try {
                if (cothem == true) //Luu cho tthem moi
                {
                    kh.InsertData(idkh, ht,sodienthoai,gt );
                    ClearData();
                    
                } else //Luu cho sua
                {
                    kh.EditData(idkh, ht,sodienthoai,gt);
                    ClearData();
                }
                setNulltf(); //goi ham xoa du lieu tron tableModel
                ShowData(); //Do lai du lieu vao Table Model
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Cap nhat that bai",
                    "Thong bao", 1);

            }
            Vohieuhoatf(false);
            Vohieuhoabt(true);
        }
        
    }//GEN-LAST:event_luuActionPerformed

